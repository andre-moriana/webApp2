                    <div class="privacy-content">
                        <h2 class="h4 mb-3">DONNÉES PERSONNELLES</h2>
                        
                        <p>Arc Training attache une importance particulière, dans le cadre de ses activités, à assurer la confidentialité des données personnelles qu'il collecte et utilise, et plus largement à la protection de la vie privée de ses adhérents notamment.</p>
                        
                        <p>Cette politique de Protection des données décrit la façon dont vos données personnelles sont collectées lorsque vous vous connectez et utilisez l'application Arc Training. Elle vous renseigne également sur la façon dont vos données personnelles sont utilisées et protégées par Arc Training, les droits dont vous disposez et vous précise comment nous contacter.</p>
                        
                        <p class="alert alert-info">
                            <i class="fas fa-info-circle me-2"></i>
                            <strong>Cette politique est un document important.</strong> Nous vous recommandons de la lire attentivement et de vérifier régulièrement si des modifications y ont été apportées.
                        </p>
                        
                        <h2 class="h4 mt-4 mb-3">NOUS CONTACTER</h2>
                        <p>Si vous avez des questions relatives à l'utilisation de vos données ou souhaitez nous faire part de vos commentaires ou préoccupations, vous pouvez contacter notre référent à la protection des données via notre <a href="/contact" class="text-success fw-bold">formulaire de contact</a>.</p>
                        
                        <h2 class="h4 mt-4 mb-3">LES DONNÉES PERSONNELLES QUE NOUS COLLECTONS</h2>
                        <p>Arc Training collecte uniquement des informations nécessaires et pertinentes et en reçoit de votre part, via notre Site ou application mobile.</p>
                        
                        <p>Dans certains cas, vous nous communiquez directement des informations personnelles, notamment lorsque vous nous contactez depuis notre formulaire de contact ou renseignez notre formulaire de préinscription.</p>
                        
                        <h3 class="h5 mt-3 mb-2">Parmi les informations que vous nous fournissez :</h3>
                        <ul>
                            <li>Les données permettant de vous identifier (nom, prénoms, civilité, date de naissance, pièce d'identité) ;</li>
                            <li>Les données de contact (adresse électronique et numéro de téléphone notamment) ;</li>
                            <li>Les données liées à votre préinscription;</li>
                        </ul>
                        
                        <p>Dans d'autres cas, nous procédons à la collecte de données en utilisant des cookies et ainsi mieux comprendre comment vous utilisez notre Site et application mobile.</p>
                        
                        <h3 class="h5 mt-3 mb-2">Parmi les données personnelles que nous collectons automatiquement et directement auprès de vous lorsque vous utilisez nos services :</h3>
                        <ul>
                            <li>L'adresse IP et les identifiants d'appareil;</li>
                            <li>Les données techniques relatives à l'appareil (nom et version du système d'exploitation de votre appareil, nom et modèle de l'appareil, version de l'Appli, langue préférée) ;</li>
                            <li>Les données techniques relatives au navigateur ou à l'appareil utilisé (nom, système d'exploitation de votre appareil, langue préférée) pour accéder à notre Site;</li>
                            <li>Les données de navigation (Dernières pages consultées, publicités cliquées, pages consultées, clics effectuées, durée de la visite, etc.).</li>
                        </ul>
                        
                        <p>Lorsque vous consultez ces services depuis notre Site, des cookies et d'autres dispositifs de suivi sont placés sur votre terminal. Nous vous recommandons de prendre connaissance de notre politique cookies. Cette page vous aidera à comprendre ce que sont les cookies et le fonctionnement des cookies déposés par le Site, de savoir également comment Arc Training les utilise et ce que vous pouvez faire quant à leur utilisation. Vous pouvez en effet, depuis la page de gestion des cookies, personnaliser vos préférences relatives aux cookies.</p>
                        
                        <h2 class="h4 mt-4 mb-3">LES DONNÉES DES ENFANTS</h2>
                        <p>Nous encourageons les parents à sensibiliser leurs enfants au sujet de la protection de leur vie privée et sur la façon dont ils communiquent sur Internet.</p>
                        
                        <p>En outre, nous respectons les dispositions légales en la matière et n'autorisons pas les enfants ayant moins de 16 ans à s'inscrire sur notre Site.</p>
                        
                        <h2 class="h4 mt-4 mb-3">COMMENT SONT UTILISÉES VOS DONNÉES</h2>
                        <p>Arc Training utilise vos données personnelles afin de :</p>
                        <ul>
                            <li>vous fournir les produits et services demandés</li>
                            <li>répondre à vos questions, résoudre votre problème, obtenir votre avis</li>
                            <li>enregistrer votre participation à un concours.</li>
                        </ul>
                        
                        <p>Nous réalisons également des traitements de données pour d'autres finalités que la stricte exécution du contrat, sous réserve de s'assurer préalablement de l'intérêt légitime à réaliser ce traitement. Les finalités pour lesquelles nous pouvons traiter vos données sont :</p>
                        <ul>
                            <li>réaliser des études et statistiques afin d'améliorer votre expérience</li>
                            <li>identifier et corriger les bugs pouvant se produire sur notre Site pour en assurer le bon fonctionnement</li>
                            <li>assurer la défense de nos droits à la défense en justice et la gestion du contentieux</li>
                            <li>garantir l'exercice de vos droits.</li>
                        </ul>
                        
                        <h2 class="h4 mt-4 mb-3">ACCÈS À VOS DONNÉES PERSONNELLES</h2>
                        <p>Arc Training est susceptible de transférer vos données personnelles à :</p>
                        <ul>
                            <li>des prestataires de services dans la mesure où ils interviennent pour la fourniture d'un produit ou service, par les services postaux et de livraison par exemple ;</li>
                            <li>des tiers nous fournissant des services informatiques, tels que des fournisseurs de plateformes, des services d'hébergement, de support technique pour nos logiciels et applications pouvant contenir des données vous concernant, l'analyse de données, la distribution des emails ;</li>
                            <li>les autorités administratives, judiciaires et de tutelle, lorsque cette communication est requise par la Loi ou liée à un impératif de sécurité.</li>
                        </ul>
                        
                        <p class="alert alert-warning">
                            <i class="fas fa-exclamation-triangle me-2"></i>
                            <strong>En revanche, aucune donnée vous concernant ne sera cédée à des partenaires à des fins de marketing ou de prospection commerciale.</strong>
                        </p>
                        
                        <p>Arc Training s'assure par ailleurs que ses partenaires et prestataires, intervenant dans le cadre de la fourniture de prestation, se conforment aux principes issus de la réglementation en vigueur.</p>
                        
                        <h2 class="h4 mt-4 mb-3">DURÉE DE CONSERVATION DE VOS DONNÉES PERSONNELLES</h2>
                        <p>Vos données personnelles seront conservées aussi longtemps que nécessaire pour réaliser les finalités exposées dans cette Politique, dans la limite des délais légaux. Ce qui signifie que nous conservons vos données personnelles au cours des trois dernières années.</p>
                        
                        <h2 class="h4 mt-4 mb-3">SÉCURITÉ DE VOS DONNÉES PERSONNELLES</h2>
                        <p>De façon générale, nous nous efforçons de protéger les données à caractère personnel en tenant compte de la sensibilité des informations en cause et des risques potentiels induits par le traitement et sa mise en œuvre. Nous mettons en œuvre tout moyen utile afin de garantir la confidentialité, l'intégrité, la disponibilité et la résilience de vos données. Nous veillons par ailleurs à stocker les données vous concernant dans des systèmes d'information offrant un niveau de sécurité appropriée, dont l'accès est protégé, restreint et enregistré.</p>
                        
                        <p>Si vous pensez que vos données personnelles sont utilisées de manière inappropriée par des tiers, nous vous remercions de nous en informer immédiatement via notre <a href="/contact" class="text-success fw-bold">formulaire de contact</a>.</p>
                        
                        <h2 class="h4 mt-4 mb-3">VOS DROITS</h2>
                        <p>Dans le respect de la réglementation, vous disposez des droits suivants :</p>
                        <ul>
                            <li><strong>Droit d'accès</strong> – vous permettant d'obtenir des informations concernant le traitement de vos données personnelles et une copie de celles-ci ;</li>
                            <li><strong>Droit de rectification</strong> – vous permettant, si vous données personnelles sont inexactes, d'exiger qu'elles soient modifiées en conséquence ; <em>Si vous avez un compte, il est simple de modifier vos données via la fonction « Mon compte ».</em></li>
                            <li><strong>Droit à l'effacement/ droit à l'oubli</strong> – vous permettant d'obtenir l'effacement ou la suppression de vos données personnelles ;</li>
                            <li><strong>Droit à la limitation</strong> – vous permettant de demander la limitation du traitement que nous effectuons sur les données vous concernant ;</li>
                            <li><strong>Droit à la portabilité</strong> – vous permettant d'obtenir une copie électronique ou le transfert des données vous concernant de notre base de données à une autre ;</li>
                            <li>Vous pouvez par ailleurs nous transmettre vos instructions pour la conservation, l'effacement ou la communication de vos données en cas de décès et désigner la personne qui en aura la charge.</li>
                        </ul>
                        
                        <p>Ces droits ne sont pas des droits absolus, mais s'exercent dans les conditions et limites fixées par la réglementation.</p>
                        
                        <p>Si vous avez consenti à recevoir nos newsletters, vous avez la possibilité de vous désinscrire en cliquant simplement sur le lien de désinscription que contient tout courriel ou toute communication que nous vous envoyons.</p>
                        
                        <p>Lors de votre première connexion sur notre Site, une bannière vous informe de la collecte de cookies et du but de cette collecte. Si vous confirmez que vous acceptez les cookies en cliquant sur le bouton "Accepter" dans cette bannière, votre consentement sera considéré comme obtenu. La bannière vous demandant d'accepter les cookies ne s'affichera donc plus sur votre écran.</p>
                        
                        <p>Si vous cliquez sur "Personnaliser", vous serez redirigé vers la page de gestion des cookies. Depuis cette page, vous avez la possibilité d'accepter ou de refuser directement tout ou partie des cookies, à l'exception des cookies fonctionnels strictement nécessaires au fonctionnement correct du Site.</p>
                        
                        <p>Quel que soit votre choix initial, vous pouvez changer d'avis à tout moment et choisir d'accepter ou de refuser tout ou partie de ces cookies directement sur le Site ou dans l'Appli. Un lien vers la page de gestion des cookies est à tout moment accessible dans le centre d'aide du Site et depuis les paramètres de l'Appli pour vous permettre de personnaliser vos préférences relatives aux cookies.</p>
                        
                        <p>Si vous êtes le représentant légal d'un enfant âgé de moins de 16 ans et pensez que votre enfant nous a fourni des informations sans votre consentement, vous pouvez nous contacter via notre <a href="/contact" class="text-success fw-bold">formulaire de contact</a> pour que nous supprimions ces informations le cas échéant.</p>
                        
                        <h2 class="h4 mt-4 mb-3">EXERCER VOS DROITS</h2>
                        <p>Vous pouvez à tout moment exercer vos droits en nous contactant notre Délégué à la Protection des Données via notre <a href="/contact" class="text-success fw-bold">formulaire de contact</a>.</p>
                        
                        <p>Votre demande doit être accompagnée de la photocopie d'un justificatif d'identité (carte nationale d'identité ou passeport, carte de résident, carte de séjour ou livret de circulation délivrés par l'Etat français ou carte d'identité de l'Union Européenne). Une réponse vous sera adressée par nos soins dans un délai d'un mois à compter de la réception de votre demande.</p>
                        
                        <p>Vous pouvez, si vous le souhaitez et conformément à la règlementation, introduire une réclamation auprès de la CNIL selon les modalités indiquées sur son site (<a href="https://www.cnil.fr" target="_blank" rel="noopener noreferrer">www.cnil.fr</a>).</p>
                        
                        <h2 class="h4 mt-4 mb-3">MODIFICATIONS DE NOTRE POLITIQUE DE PROTECTION DES DONNÉES PERSONNELLES</h2>
                        <p>La réglementation et les usages évoluent dans le temps. Ainsi, nous nous réservons le droit d'apporter des modifications à la Politique de protection des données personnelles à tout moment pour les adapter à ces changements.</p>
                        
                        <p>Dans ce cadre, la Politique de protection des données personnelles sera republiée sur notre Site pour que vous puissiez directement prendre connaissance.</p>
